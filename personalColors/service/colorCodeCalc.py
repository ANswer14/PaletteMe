def calc_color_codes(color):
    if color == '코랄, 피치, 맑은 오렌지, 라임':
        return ['#e68070', '#f297cf', '#00ff00']
    elif color == '살구, 소프트 코랄, 크림 옐로우':
        return ['#fbceb1', '#eaac9c', '#fff5a6']
    elif color == '브릭 레드, 선명한 브라운, 딥 오렌지':
        return ['#aa4a44', '#8b4513', '#dc4d01']
    elif color == '카멜, 모카, 올리브, 웜 와인':
        return ['#c19a6b', '#967969', '#808000']
    elif color == '라이트 코랄핑크, 클린 레드(노랑/블루기 적은 레드), 애플그린, 스카이블루':
        return ['#f9c1bb', '#a33b39', '#8db600']
    elif color == '살구, 소프트 코랄, 크림 옐로우, 라이트 베이지, 더스티 로즈, 연한 그레이':
        return ['#d0baa2', '#c9a9a6', '#d3d3d3']
    elif color == '뉴트럴 레드, 차콜, 딥 그레이, 쿨&웜 중간의 네이비, 다크 에메랄드':
        return ['#ab6465', '#36454f', '#4e4e4e']
    elif color == '토프, 그레이지, 소프트 브라운, 더스티 카키, 로즈 브라운, 뮤트 네이비':
        return ['#483c32', '#b0a999', '#a67060']
    elif color == '쿨 핑크, 라즈베리, 민트, 라일락':
        return ['#ff1493', '#e30b5d', '#01dce3']
    elif color == '로즈베이지, 그레이 핑크, 소프트 블루':
        return ['#c69294', '#ffd1dc', '#abd7eb']
    elif color == '트루 레드, 블랙, 코발트, 퓨어 화이트':
        return ['#ff0000', '#0047ab', '#ffffff']
    elif color == '버건디, 차콜, 딥 플럼, 스모키 네이비':
        return ['#760c0c', '#36454f', '#2f3a4a']
